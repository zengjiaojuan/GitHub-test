package com.phb.puhuibao.entity;
 

public class ExperienceValue {
	
	 
	private Integer   experienceId;//```experience_id```
	private Integer   mUserId;//```m_user_id```
	private String    experienceType;//``experience_type``
	private Double    experienceValue;//```experience_value```
	
	public Integer getExperienceId() {
		return experienceId;
	}
	public void setExperienceId(Integer experienceId) {
		this.experienceId = experienceId;
	}
	public Integer getmUserId() {
		return mUserId;
	}
	public void setmUserId(Integer mUserId) {
		this.mUserId = mUserId;
	}
 
	public String getExperienceType() {
		return experienceType;
	}
	public void setExperienceType(String experienceType) {
		this.experienceType = experienceType;
	}
	public Double getExperienceValue() {
		return experienceValue;
	}
	public void setExperienceValue(Double experienceValue) {
		this.experienceValue = experienceValue;
	}
 
	
	 
	 
	 
	
	
}
