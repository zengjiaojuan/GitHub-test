package com.idap.clinic.entity;

public class EveryPic {
	private String everyPicId;
	private int everyWeight;
	private String everyTitle;
	private String everyContent;
	private String everyPic;
	
	public String getEveryPicId() {
		return everyPicId;
	}
	public void setEveryPicId(String everyPicId) {
		this.everyPicId = everyPicId;
	}
	public int getEveryWeight() {
		return everyWeight;
	}
	public void setEveryWeight(int everyWeight) {
		this.everyWeight = everyWeight;
	}
	public String getEveryTitle() {
		return everyTitle;
	}
	public void setEveryTitle(String everyTitle) {
		this.everyTitle = everyTitle;
	}
	public String getEveryContent() {
		return everyContent;
	}
	public void setEveryContent(String everyContent) {
		this.everyContent = everyContent;
	}
	public String getEveryPic() {
		return everyPic;
	}
	public void setEveryPic(String everyPic) {
		this.everyPic = everyPic;
	}
}
